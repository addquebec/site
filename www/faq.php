          <h4 class="showSingle" >Vais-je apprendre à faire des acrobaties?<span class="iconic" style="float:right;color:white;font-size:37px;margin-top:12.5px;">%</span></h4>
          <ul class="targetDiv"><li>Tout dépend de ce que vous entendez par « acrobaties ». L’art du
                                                déplacement ne se résume pas à de la gymnastique, et de nombreux 
                                                pratiquants de très haut niveau ne feront jamais, par exemple, de saltos.
                                                Certains y voient toutefois une façon de développer leur créativité, et
                                                rien n’exclut qu’après l’expérience obtenue au fil des entrainements, 
                                                des ateliers spécifiques leur soient offerts pour apprendre quelques 
                                                acrobaties des plus renversantes… Dans les cours réguliers toutefois, 
                                                cela se résume à des roulades au sol, des passements d’obstacles, des 
                                                exercices parfaitement sécuritaires pour favoriser la coordination, 
                                                etcétéra.</li></ul><br/>

          <h4 class="showSingle" >Qui sont les Yamakasi?<span class="iconic" style="float:right;color:white;font-size:37px;margin-top:12.5px;">%</span></h4>
          <ul class="targetDiv"><li>« Yamakasi » vient du zaïrois et signifie « homme fort, esprit fort, corps
                                                fort ». Dans l’ADD, cette notion de force est intimement liée à celles de
                                                courage, d’entraide, de partage, de respect, de maturité. « Yamakasi »,
                                                c’est le nom que s’est donné le groupe à l’origine de la pratique de 
                                                l’art du déplacement. Unis par ces valeurs, ceux qui n’étaient que 
                                                des jeunes « banlieusards » de la région parisienne sont devenus des 
                                                athlètes-artistes mondialement reconnus; ils ont peaufiné leur art 
                                                pendant plus de deux décennies et certains d’entre eux, à l’origine 
                                                notamment de l’ADD Academy en France, ont conçu un programme 
                                                pédagogique global, immensément riche. L’Académie québécoise d’art 
                                                du déplacement, pendant québécois de cette mythique école française, 
                                                partage à son tour cette passion pour le mouvement.</li></ul><br/>

